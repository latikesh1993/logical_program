package find_total_no_of_white_spaces_in_String;

public class sample1 {
	public static void main(String[] args) {
		
	  String s1 = "Latikesh Nanasaheb Bhosale";
	  
	  int count=0;
	  
		for(int i=0; i<=s1.length()-1; i++) {
			
			char s2=s1.charAt(i);
			
			if(s2==' ') {
				
				count++;
			}
			
			
			
		}
		
		System.out.println("Spacing in given string:"+count);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
	

}
